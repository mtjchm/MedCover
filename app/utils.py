# ── Czech locale-aware sorting ────────────────────────────────────────────────

# PostgreSQL ICU collation name to use in all order_by() calls where
# user-visible names are sorted.  cs-x-icu provides proper Czech alphabet
# ordering (a á b c č d ď e é ě … ch … ř š … ž) within the DB engine.
CS_COLLATION = "cs-x-icu"

# Czech alphabet in correct order including digraph 'ch'.
_CS_ALPHABET: list[str] = [
    "a", "á", "b", "c", "č", "d", "ď", "e", "é", "ě",
    "f", "g", "h", "ch", "i", "í", "j", "k", "l", "m",
    "n", "ň", "o", "ó", "p", "q", "r", "ř", "s", "š",
    "t", "ť", "u", "ú", "ů", "v", "w", "x", "y", "ý",
    "z", "ž",
]
_CS_RANK: dict[str, int] = {ch: i for i, ch in enumerate(_CS_ALPHABET)}
_CS_FALLBACK = len(_CS_ALPHABET)


def czech_sort_key(text: str) -> list[int]:
    """Return a sort-key list for Czech locale-aware string comparison.

    Handles the 'ch' digraph (sorts between 'h' and 'i') and all standard
    Czech diacritic letters.  Use as the ``key=`` argument for Python-level
    sorts that cannot be pushed to the database.

    Example::

        users.sort(key=lambda u: czech_sort_key(u.name))
    """
    s = text.lower()
    result: list[int] = []
    i = 0
    while i < len(s):
        if s[i] == "c" and i + 1 < len(s) and s[i + 1] == "h":
            result.append(_CS_RANK["ch"])
            i += 2
        else:
            result.append(_CS_RANK.get(s[i], _CS_FALLBACK + ord(s[i])))
            i += 1
    return result


def safe_next(next_url: str | None) -> str:
    """Return *next_url* only if same-origin; otherwise fall back to dashboard.

    Prevents open-redirect attacks where an attacker supplies an absolute URL
    as the ``next`` parameter (e.g. ``?next=https://evil.example``).
    """
    from flask import url_for

    if not next_url:
        return url_for("main.dashboard")
    parsed = urlsplit(next_url)
    if parsed.scheme or parsed.netloc:
        return url_for("main.dashboard")
    return next_url


def external_url_for(endpoint: str, **values: object) -> str:
    """Build an absolute URL for *endpoint*, honouring the configured ``app_base_url``.

    When ``AppSettings.app_base_url`` is set (e.g. ``https://medcoverdev.spidermila.site``),
    that base is prepended to the path generated by Flask's ``url_for``.  This ensures that
    links in e-mails (password reset, invitations, activation notices) point to the
    publicly accessible address even when the app runs behind a reverse proxy.

    Falls back to Flask's built-in ``_external=True`` behaviour when no base URL is
    configured so that local / test environments still produce valid links.
    """
    from flask import url_for

    try:
        from app.models.settings import get_settings
        base: str | None = get_settings().app_base_url
    except Exception:
        base = None

    path = url_for(endpoint, **values)  # type: ignore[arg-type]
    if base:
        return base.rstrip("/") + path
    return url_for(endpoint, _external=True, **values)  # type: ignore[arg-type]


def diff_changes(before: dict, after: dict) -> dict:
    """Return {field: [old_val, new_val]} for fields whose values changed.

    Compares using string representation so None and '' differences are caught.
    Only includes fields that actually changed — fields with identical values
    before and after are omitted.

    Example:
        before = {"name": "Old", "desc": None}
        after  = {"name": "New", "desc": None}
        → {"name": ["Old", "New"]}
    """
    all_keys = set(list(before.keys()) + list(after.keys()))
    return {
        k: [before.get(k), after.get(k)]
        for k in sorted(all_keys)
        if str(before.get(k)) != str(after.get(k))
    }


def audit(
    action: str,
    entity_type: str,
    entity_id: str | int,
    summary: str,
    changes: dict | None = None,
) -> None:
    """Append an ``AuditLogEntry`` for the current user to the active DB session.

    The caller is responsible for committing. Use within a transaction so that
    the audit row is rolled back together with the business change on failure.
    """
    from flask_login import current_user
    from app.extensions import db
    from app.models.audit import AuditLogEntry

    db.session.add(AuditLogEntry(
        actor_id=current_user.id,
        action_type=action,
        entity_type=entity_type,
        entity_id=str(entity_id),
        summary=summary,
        changes_json=changes,
    ))


def get_or_404(model: type[T], pk: object) -> T:
    """Fetch *model* by primary key; abort 404 if missing.

    Standard idiom for view functions to resolve a URL parameter to an entity.
    """
    from flask import abort
    from app.extensions import db

    obj = db.session.get(model, pk)
    if obj is None:
        abort(404)
    return obj  # type: ignore[return-value]


def require_permission(*codes: str) -> None:
    """Abort 403 unless the current user holds at least one of *codes*.

    Call inline at the top of view functions that already use ``@login_required``.
    """
    from flask import abort
    from flask_login import current_user

    if not current_user.has_any_permission(*codes):
        abort(403)


def check_version_conflict(obj: object, form_value: str | None) -> bool:
    """Return True if the form's submitted version doesn't match ``obj.version``.

    Used for optimistic-locking edit forms — caller flashes
    :data:`RECORD_MODIFIED_MSG` and re-renders the form when this returns True.
    """
    try:
        submitted = int(form_value or 0)
    except (TypeError, ValueError):
        submitted = 0
    current = getattr(obj, "version", None)
    return current is not None and submitted != current


def parse_enum(enum_class: type[E], value: object, default: E | None = None) -> E | None:
    """Coerce *value* to *enum_class*; return *default* on failure."""
    if value is None:
        return default
    try:
        return enum_class(value)  # type: ignore[call-arg]
    except (ValueError, KeyError):
        return default
